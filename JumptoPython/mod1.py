def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    return a - b

def pow(a, b):
    return a ** b

if __name__ == "__main__":
    print('-' * 15)
    print('called! __main__!')
    print(add(5, 5))
    print(sub(15, 5))
    print(mul(5, 5))
    print(div(10, 2))
    print(pow(5, 3))
